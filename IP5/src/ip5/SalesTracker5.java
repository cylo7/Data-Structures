    private void displayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayButtonActionPerformed
        displayOutput.setText("");
        SalesPersonReader reader = new SalesPersonReader();
        try {
            Collection<SalesPerson> people = reader.readPeople("stars.txt");
            people.stream().map((SalesPerson p) -> {
                displayOutput.append(p.toString());
                return p;
            }).forEachOrdered((SalesPerson _item) -> {
                displayOutput.append("\n");
            });

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Unable to open file");
            Logger.getLogger(SalesTracker5.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_displayButtonActionPerformed

    private void evaluateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_evaluateButtonActionPerformed
        displayOutput.setText("");
        SalesPersonReader reader = new SalesPersonReader();
        try {
            Collection<SalesPerson> people = reader.readPeople("salesperson.txt");
            StarSalesPerson ssp = new StarSalesPerson();         
            displayOutput.append(ssp.star(people).toString());
            displayOutput.append("\n");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Unable to open file");
            Logger.getLogger(SalesTracker5.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_evaluateButtonActionPerformed

