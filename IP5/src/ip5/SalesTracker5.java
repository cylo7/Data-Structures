    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        boolean allDataRecieved = true;
        SalesPerson p = new SalesPerson();

        String empId = salesIdInput.getText();
        try {
            int salesId = Integer.parseInt(empId);
            if (salesId <= 0) {
                idError.setVisible(true);
                allDataRecieved = false;
            } else {
                p.setSalesId(salesId);
                idError.setVisible(false);
            }
        } catch (NumberFormatException e) {
            idError.setVisible(true);
            allDataRecieved = false;
        }

        String firstName = firstNameInput.getText();

        if (firstName.trim().length() == 0) {
            fNameError.setVisible(true);
            allDataRecieved = false;
        } else {
            firstNameInput.setText(firstName.trim());
            p.setFirstName(firstName.trim());
            fNameError.setVisible(false);

        }

        String lastName = lastNameInput.getText();

        if (lastName.trim().length() == 0) {
            lNameError.setVisible(true);
            allDataRecieved = false;
        } else {
            lastNameInput.setText(lastName);
            p.setLastName(lastName.trim());
            lNameError.setVisible(false);
        }

        String totalSupplies = suppliesInput.getText();
        try {
            double supplies = Double.parseDouble(totalSupplies);
            if (supplies <= 0) {
                p.setSupplies(0.00);
            } else {
                p.setSupplies(supplies);
                suppliesError.setVisible(false);
            }
        } catch (NumberFormatException e) {
            suppliesError.setVisible(true);
            allDataRecieved = false;
        }

        String totalBooks = booksInput.getText();
        try {
            double books = Double.parseDouble(totalBooks);
            if (books <= 0) {
                p.setBooks(0.00);
            } else {
                p.setBooks(books);
                booksError.setVisible(false);
            }
        } catch (NumberFormatException e) {
            booksError.setVisible(true);
            allDataRecieved = false;
        }

        String totalPaper = paperInput.getText();
        try {
            double paper = Double.parseDouble(totalPaper);
            if (paper <= 0) {
                p.setPaper(0.00);
            } else {
                p.setPaper(paper);
                paperError.setVisible(false);
            }
        } catch (NumberFormatException e) {
            paperError.setVisible(true);
            allDataRecieved = false;
        }

        // Combo box action
        if (districtComboBox != null) {
            String district = (districtComboBox.getSelectedItem().toString());
            p.setDistrict(district);
        }
        // Radio Button selection
        if (buttonGroup1.getSelection() != null) {
            String contact = (buttonGroup1.getSelection().toString());
            if (phoneRadioButton.isSelected()) {
                p.setContact("PHONE");
            } else if (emailRadioButton.isSelected()) {
                p.setContact("EMAIL");
            } else if (meetingRadioButton.isSelected()) {
                p.setContact("MEETING");
            }
        }
        p.calculateTotals();
        if (allDataRecieved) {
            try {
                SalesPersonStorage pfs = new SalesPersonStorage();
                pfs.writePerson(p);

                successLabel.setVisible(true);
                salesIdInput.setText("");
                firstNameInput.setText("");
                lastNameInput.setText("");
                suppliesInput.setText("");
                booksInput.setText("");
                paperInput.setText("");

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error writing to file");
                Logger.getLogger(SalesTracker5.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

